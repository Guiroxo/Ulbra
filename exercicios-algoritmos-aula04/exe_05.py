print('Média dos números reais escolhidos')
numero1 = float(input('Escolha seu primeiro número: '))
numero2 = float(input('Escolha seu segundo número: '))
numero3 = float(input('Escolha seu terceiro número: '))
media = (numero1 + numero2 + numero3) / 3
print(f'Os números escolhidos foram {numero1}, {numero2} e {numero3} a média desses números é {media}')